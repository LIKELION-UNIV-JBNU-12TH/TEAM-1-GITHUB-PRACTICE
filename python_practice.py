# 두 숫자를 더하는 함수
def add_numbers():
    # 사용자로부터 숫자 입력 받기
    num1 = float(input("첫 번째 숫자를 입력하세요: "))
    num2 = float(input("두 번째 숫자를 입력하세요: "))
    
    # 두 숫자를 더하고 결과 출력
    result = num1 + num2
    print("두 숫자의 합은:", result)

# 함수 호출하여 프로그램 실행
add_numbers()

